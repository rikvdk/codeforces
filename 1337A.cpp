#include <algorithm>
#include <cmath>
#include <iostream>
#include <string>

using namespace std;

typedef long long ll;

int main()
{
    int t;
    ll a, b, c, d;
    cin >> t;
    for(int i = 0; i < t; ++i) {
        cin >> a >> b >> c >> d;
        cout << b << ' ' << c << ' ' << c << '\n';
    }
}
